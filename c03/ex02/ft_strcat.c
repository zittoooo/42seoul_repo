char	*ft_strcat(char *dest, char *src)
{
	char *ret;
	int i;
	int j;

	ret = dest;
	i = 0;
	j = 0;
	while (src[i] != '\0')
		i++;
	while (dest[j] != '\0')
		dest++;
	while (i-- > 0)
		*dest++ = *src++;
	*dest = '\0';
	return (ret);
}
