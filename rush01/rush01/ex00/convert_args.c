void	convert_args(int views[16], char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (str[i] == 32)
		{
		}
		else if (48 < str[i] && str[i] < 53)
		{
			views[j] = str[i] - 48;
			j++;
		}
		i++;
	}
}
